public class Deadline extends Task{
    protected String by;

    public Deadline(String description, String by) {
        super(TaskType.DEADLINE,description);
        this.by = by;
    }

    public String getBy() {
        return by;
    }

    @Override
    public String toString() {
        return super.toString() + " (by: " + by + ")";
    }
}
